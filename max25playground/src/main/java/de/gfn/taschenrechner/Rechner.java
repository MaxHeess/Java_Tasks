package de.gfn.taschenrechner;

public class Rechner {


    public int addition(int zahl, int zahl2) {
        return zahl + zahl2;
    }

    public int subtraktion(int zahl, int zahl2) {
        return zahl - zahl2;
    }

    public int multiplikation(int zahl, int zahl2) {
        return zahl * zahl2;
    }

    public int division(int zahl, int zahl2) {
        return (int)(zahl / zahl2);
    }

}
